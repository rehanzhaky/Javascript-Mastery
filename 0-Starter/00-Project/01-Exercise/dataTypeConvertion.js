// inisiasi satu variabel dengan nilai "100"
// konversikan variabel diatas menjadi tipe data number lalu tambah dengan angka 50
// inisiasi satu variabel dengan nilai "true"
// konversikan variabel diatas menjadi tipe data boolean

let nilai = "100";
let nilaiKonversi = Number(nilai);
console.log(`Hasil : ${nilaiKonversi + 50}`);

let fakta = "true";
let faktaKonversi = Boolean(fakta);
console.log(`Hasil : ${faktaKonversi}`);
