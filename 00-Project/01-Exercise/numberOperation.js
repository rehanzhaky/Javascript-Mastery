// inisiasi dua variabel dengan nilai 10 dan 3
// lakukan operasi penjumlahan, pengurangan, perkalian, pembagian, modulus
// cetak semua hasil

let nilai1 = 10;
let nilai2 = 3;

let penjumlahan = nilai1 + nilai2;
let pengurangan = nilai1 - nilai2;
let perkalian = nilai1 * nilai2;
let pembagian = nilai1 / nilai2;
let modulus = nilai1 % nilai2;

console.log(penjumlahan);
console.log(pengurangan);
console.log(perkalian);
console.log(pembagian);
console.log(modulus);
